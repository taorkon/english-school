
function ShowModal(elId) {
    var modalAll = document.getElementById(elId);
    modalAll.style.display = "flex";
  }
  
  function HideModal() {
    if (event.target.classList.contains('modal')) {
      event.currentTarget.style.display = "none";
    }
  }


